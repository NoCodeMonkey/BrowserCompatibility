namespace BrowserCompatibility
{
	/// <summary>
	/// Browser Feature Status Enumeration
	/// </summary>
	public enum BrowserFeatureStatus
	{
		/// <summary>
		/// Unknown Status - The status of the browser feature is unknown or untested
		/// </summary>
		Unknown,
		/// <summary>
		/// Enabled Status -  The feature is enabled in the browser
		/// </summary>
		Enabled,
		/// <summary>
		/// Disabled Status -  The feature is disabled in the browser
		/// </summary>
		Disabled
	}
}
